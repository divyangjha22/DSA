#include<iostream>
using namespace std;

int main(){
    // Implicit Type casting
    
    char ch=98; // Here 98 is the ASCII value of b. 
    cout<<ch<<endl;
    cout<<" "<<endl;
    
    int a='b';
    cout<<a<<endl;
    cout<<" "<<endl;
    
    // Explicit Typecasting
    double d=5.7;
    int x=d;
    cout<< x<<endl;
    cout<<" "<<endl;
    

    return 0;
}