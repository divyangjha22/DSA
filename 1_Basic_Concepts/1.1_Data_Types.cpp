#include<iostream>
using namespace std;

int main(){
    int a=5;
    cout<<"The value of a is "<<a<<endl;
    cout<<sizeof(a)<<endl;

    char ch='a';
    cout<<"The value of ch is "<<ch<<endl;
    cout<<sizeof(ch)<<endl;

    bool flag=true;
    cout<<"The value of flag is "<<flag<<endl;
    cout<<sizeof(flag)<<endl;

    short s=5;
    cout<<"The value of a is "<<s<<endl;
    cout<<sizeof(s)<<endl;

    float f=5.2;
    cout<<"The value of a is "<<f<<endl;
    cout<<sizeof(f)<<endl;

    double d=5;
    cout<<"The value of a is "<<d<<endl;
    cout<<sizeof(d)<<endl;

    long l=5;
    cout<<"The value of a is "<<l<<endl;
    cout<<sizeof(l)<<endl;

    long long ll=5;
    cout<<"The value of a is "<<ll<<endl;
    cout<<sizeof(ll)<<endl;

    return 0;
}